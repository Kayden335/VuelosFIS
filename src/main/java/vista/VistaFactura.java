    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String contenido = txtAreaTicket.getText();
        if (contenido == null || contenido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay datos para guardar");
            return;
    }//GEN-LAST:event_btnGuardarActionPerformed
    JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleccione dónde guardar su factura");
        fileChooser.setSelectedFile(new File("Factura_Vuelo.txt"));

        int userSelection = fileChooser.showSaveDialog(this);

        // Usamos 0 que es el valor de JFileChooser.APPROVE_VALUE
        if (userSelection == 0) { 
            File archivoAguardar = fileChooser.getSelectedFile();
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivoAguardar))) {
                writer.write(contenido);
                JOptionPane.showMessageDialog(this, "Factura guardada con éxito en:\n" + archivoAguardar.getAbsolutePath());
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al guardar el archivo: " + e.getMessage());
            }
        }
    }                                          
