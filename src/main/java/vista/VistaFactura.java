    private void generarFactura() {
    StringBuilder sb = new StringBuilder();

    sb.append("========= FACTURA DE VUELO =========\n\n");

    int i = 1;
    for (ModeloBoleto b : carrito.getBoletos()) {
        sb.append("Boleto ").append(i++).append("\n");
        sb.append("Nombre: ").append(b.getNombre()).append("\n");
        sb.append("Clase: ").append(b.getClaseVuelo()).append("\n");
        sb.append("Vuelo: ").append(b.getVueloSeleccionado()).append("\n");
        sb.append("Tipo: ").append(b.getTipoVuelo()).append("\n");
        sb.append("Maletas: ").append(b.isTieneMaletas() ? "Sí" : "No").append("\n");
        sb.append("Precio: $").append(b.getPrecio()).append("\n");
        sb.append("----------------------------------\n");
    }

    sb.append("\nTOTAL PAGADO: $")
      .append(String.format("%.2f", carrito.getTotal()));

    setTextoTicket(sb.toString());
}

